import java.util.Scanner;


public class Day7dowhilefor {

	public static void main(String[] args) {
		
		// program koji izlistava trazene brojeve
				
		int counter = 0;
		while(counter<6) {
			System.out.println(counter);
			counter++;
			
		}
		
		// varijanta 2 (FOR) programa koji izlistava trazene brojeve
		for(int i=6; i>0; --i) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
	}

}
